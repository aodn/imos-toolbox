classdef testReadWQMraw < matlab.unittest.TestCase

    % Test Reading WQM raw files with the
    % readWQMraw function.
    %
    %
    % author: hugo.oliveira@utas.edu.au
    %

    properties (TestParameter)
        mode = {'timeSeries'};
        wqm_file = files2namestruct(FilesInFolder([toolboxRootPath 'data/testfiles/Sea_Bird_Scientific/WQM/RAW/v000']));
    end

    methods (Test)

        function testReadSerialNumber(~, wqm_file, mode)
            dict = load_table();
            data = readWQMraw(wqm_file, mode);
            [~, filename, ext] = fileparts(wqm_file);
            key = [filename ext];

            if dict.isKey(key)
                assert(strcmpi(data.meta.instrument_serial_no, string(dict(key))));
            else
                warning('not validating instrumentmodel - file %s not registred in the table', key)
            end

        end

    end

end

